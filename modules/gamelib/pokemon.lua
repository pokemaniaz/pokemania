    if typesEffectiveness[a].weak and type2 and isInArray(typesEffectiveness[a].weak, type2) then
      multiplier = multiplier / 2
    end
    if typesEffectiveness[a].non and isInArray(typesEffectiveness[a].non, type1) then
      multiplier = 0
    end
    if typesEffectiveness[a].non and type2 and isInArray(typesEffectiveness[a].non, type2) then
      multiplier = 0
    end
    if multiplier == effectiveness then
      table.insert(ret, a..'-')
    end
  end

  return table.concat(ret)
end

function doCorrectString(str)
  local name = str:explode(' ')
  local final = {}
  for _, s in ipairs(name) do
    table.insert(final, s:sub(1, 1):upper()..s:sub(2, #s):lower())
  end
  return table.concat(final, (name[2] and ' ' or ''))
end

function isPassive(skill)
  for i = 1, #pokemonPassives do
    if pokemonPassives[i] == skill then
      return true
    end
  end
  return false
end

function getPokemonSkills(name)
  local str = ""
  for a, b in pairs(specialabilities) do
    for i = 1, #b do
      if(b[i] == name) then
        if not isPassive(a) then
          str = str .. (str == "" and "" or ", ") .. a
        end
      end
    end
  end
  if str == "" then
    str = "None"
  end
  return doCorrectString(str)
end

function getPokemonIdByName(name)
  for i = 1, #pokemonId do
    if pokemonId[i] == name then
      return i
    end
  end
  return 1
end

function getPokemonNameByOutfit(outfit)
  for a, b in pairs(pokemonsOutfit) do
    if b == outfit then
      return doCorrectString(a)
    end
  end
  return 'Bulbasaur'
end

